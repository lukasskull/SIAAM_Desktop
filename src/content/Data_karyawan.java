    private void bTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTambahActionPerformed
        String cekUser = tUser.getText().trim().replaceAll("\\s","");
        String cekPass = tPass.getText().trim().replaceAll("\\s","");;
        String cekNama = tNama.getText();
        String cekRole = cRole.getSelectedItem().toString();
        String cekHp = tHp.getText().trim().replaceAll("\\s","");
        String cekOutlet = cOutlet.getSelectedItem().toString();
        int generatedKey = 0;
        
        
        if(cekUser.equals("")&&cekPass.equals("")&&cekNama.equals("")&&cekRole.equals("Pilih Role Karyawan")&&cekOutlet.equals("Pilih Outlet")&&cekHp.equals("")){
          JOptionPane.showMessageDialog(null, "Isi Data Terlebih Dahulu!");
          JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
        } 
        else if(cekNama.equals("")){
            JOptionPane.showMessageDialog(null, "Nama Dokter Tidak Boleh Kosong!");
            JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
        } else if(cekUser.equals("")){
            JOptionPane.showMessageDialog(null, "Username Tidak Boleh Kosong!");
            JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
        } else if(cekPass.equals("")){
            JOptionPane.showMessageDialog(null, "Password Tidak Boleh Kosong!");
            JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
        }else if(cekHp.equals("")){
            JOptionPane.showMessageDialog(null, "Nomor Telepon Tidak Boleh Kosong!");
            JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
        }else if(cekHp.length()>12){
            JOptionPane.showMessageDialog(null, "Nomor Telepon Maksimal 12 Digit!");
            JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
        }else if(!cekHp.matches("[0-9]+")){
            JOptionPane.showMessageDialog(null, "Format Nomor Telepon Adalah Angka!");
            JOptionPane.showMessageDialog(null, "Data Gagal Ditambahkan!");
        }else {
             if(JOptionPane.showConfirmDialog(null, "Anda Yakin Data Yang Anda Tambahkan Sudah Benar?","Menambahkan Data",JOptionPane.YES_NO_OPTION)== JOptionPane.YES_OPTION){  
                 try{
                        st = conn.createStatement();
                        String ifExsist = "SELECT * FROM sys_karyawan WHERE karyawan_username = '"+cekUser+"' && karyawan_name = '"+tNama.getText()+"' && karyawan_role_id = '"+idRole+"' && karyawan_phone = '"+cekHp+"' && karyawan_outlet_id = '"+idOutlet+"' ";
                        rs = st.executeQuery(ifExsist);
                        if(rs.next()){
                            JOptionPane.showMessageDialog(null,"Data Sudah Pernah Disimpan!");
                            JOptionPane.showMessageDialog(null,"Data Batal Disimpan!");
                        }
                        else{   
                                try{
                                    String sql = "INSERT INTO sys_karyawan VALUES (NULL,'"+cekUser+"', '"+tNama.getText()+"','"+idRole+"', '"+cekHp+"', '"+idOutlet+"')";
                                    PreparedStatement ps = conn.prepareStatement(sql,Statement.RETURN_GENERATED_KEYS);
                                    ps.execute();
                                    ResultSet rs = ps.getGeneratedKeys();
                                    if(rs.next()){
                                       generatedKey = rs.getInt(1);
                                    }
                                   }catch(Exception e){
                                       System.out.println(e.getMessage());
                                  }
                                


                                   try{    
                                     String sql = "INSERT INTO sys_password VALUES (NULL,MD5('"+cekPass+"'), '"+generatedKey+"')";
                                     st = conn.createStatement();
                                     st.executeUpdate(sql);

                                     JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan!");
                                     clearText();
                                     tampilData();

                                  }catch(Exception e){
                                      System.out.println("Error 1");
                                  }
                        }
                 }catch(Exception e){
                     System.out.println("Error 2");
                 }
             }else{
                   JOptionPane.showMessageDialog(null,"Data Batal Disimpan!");
             }
        }
    }//GEN-LAST:event_bTambahActionPerformed

